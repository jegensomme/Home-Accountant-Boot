package ru.jegensomme.homeaccountant.util;

import lombok.experimental.UtilityClass;
import org.slf4j.Logger;
import org.springframework.lang.NonNull;
import ru.jegensomme.homeaccountant.Identified;
import ru.jegensomme.homeaccountant.util.exception.ErrorType;
import ru.jegensomme.homeaccountant.util.exception.IllegalRequestDataException;
import ru.jegensomme.homeaccountant.util.exception.NotFoundException;

import javax.servlet.http.HttpServletRequest;

@UtilityClass
public class ValidationUtil {
    @NonNull
    public static <T> T checkNotFoundWithId(T object, int id) {
        checkNotFoundWithId(object != null, id);
        return object;
    }

    public static void checkNotFoundWithId(boolean found, int id) {
        checkNotFound(found, "id=" + id);
    }

    @NonNull
    public static <T> T checkNotFound(T object, String msg) {
        checkNotFound(object != null, msg);
        return object;
    }

    public static void checkNotFound(boolean found, String msg) {
        if (!found) {
            throw new NotFoundException("Not found entity with " + msg);
        }
    }

    public static void checkNew(@NonNull Identified entity) {
        if (!entity.isNew()) {
            throw new IllegalRequestDataException(entity + " must be new (id=null)");
        }
    }

    public static void assureIdConsistent(@NonNull Identified entity, int id) {
//      conservative when you reply, but accept liberally (http://stackoverflow.com/a/32728226/548473)
        if (entity.isNew()) {
            entity.setId(id);
        } else if (entity.id() != id) {
            throw new IllegalRequestDataException(entity + " must be with id=" + id);
        }
    }

    //  http://stackoverflow.com/a/28565320/548473
    public static Throwable getRootCause(@NonNull Throwable t) {
        Throwable result = t;
        Throwable cause;

        while (null != (cause = result.getCause()) && (result != cause)) {
            result = cause;
        }
        return result;
    }

    public static String getMessage(@NonNull Throwable e) {
        return e.getLocalizedMessage() != null ? e.getLocalizedMessage() : e.getClass().getName();
    }

    public static Throwable logAndGetRootCause(@NonNull Logger log,
                                               @NonNull HttpServletRequest req,
                                               @NonNull Exception e,
                                               boolean logStackTrace,
                                               @NonNull ErrorType errorType) {
        Throwable rootCause = ValidationUtil.getRootCause(e);
        if (logStackTrace) {
            log.error(errorType + " at request " + req.getRequestURL(), rootCause);
        } else {
            log.warn("{} at request  {}: {}", errorType, req.getRequestURL(), rootCause.toString());
        }
        return rootCause;
    }
}